<script>
  // JavaScript code for making the page interactive

  // Shopping Cart functionality
  let shoppingCart = [];

  function addToCart(productName, price) {
      shoppingCart.push({ productName, price });
      alert(`Added ${productName} to the cart. Price: ${price}`);
  }

  // Update the cart UI
  function updateCartUI() {
      const cartItemsContainer = document.getElementById("cartItems");
      cartItemsContainer.innerHTML = ""; // Clear previous cart items

      shoppingCart.forEach(item => {
          const cartItem = document.createElement("div");
          cartItem.textContent = `${item.productName} - $${item.price}`;
          cartItemsContainer.appendChild(cartItem);
      });
  }

  // Event listener for Add to Cart buttons
  const addToCartButtons = document.querySelectorAll(".pro a");
  addToCartButtons.forEach(button => {
      button.addEventListener("click", function(event) {
          event.preventDefault(); // Prevent the default link behavior
          const product = event.currentTarget.parentElement.querySelector("h5").textContent;
          const price = event.currentTarget.parentElement.querySelector("h4").textContent;
          addToCart(product, price);
          updateCartUI();
      });
  });

  // Clear the cart
  const clearCartButton = document.getElementById("clearCart");
  clearCartButton.addEventListener("click", function() {
      shoppingCart = [];
      updateCartUI();
  });

  // More JavaScript code can be added for additional functionality
